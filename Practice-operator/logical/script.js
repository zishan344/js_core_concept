// (&&) operator
{
  console.log(30 > 20 && 10 < 20); // 1
  console.log(30 > 20 && 30 < 20); // 2
  console.log(10 > 20 && 20 < 20); // 3
}

// (||) operator
{
  console.log(30 > 20 || 10 < 20); // 1
  console.log(30 > 20 || 30 < 20); // 2
  console.log(5 > 20 || 30 < 20); // 3
}
// (!) operator
{
  console.log(30 > 20); // 1
  console.log(20 > 30); // 2
}
