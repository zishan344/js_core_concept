// 0 start the assignment operator with vs code

//  1 (=) simpale assignment operator
{
  var a = 10;
  var b = 20;
  var c = a + b;
  console.log(c);
}

//  2 (+=) plus equal operator
{
  var a = 20;
  a += 2;
  console.log(a);
}

// 3 (-=) minus equal operator
{
  var a = 20;
  a -= 2;
  console.log(a);
}

// 4 (*=) into equal
{
  var a = 20;
  a *= 2;
  console.log(a);
}

// 5 ( / ) divide equal
{
  var a = 20;
  a /= 2;
}

// 6 (%=) mod equal
{
  var a = 20;
  a %= 3;
  console.log(a);
}

